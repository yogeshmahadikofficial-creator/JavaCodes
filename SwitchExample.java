public class SwitchExample{
    public static void main(String args[]){
        int number=1;
        switch(number){
            case 1: System.out.println("Tea");
            break;
            case 2: System.out.println("Coffee");
            break;
            case 3: System.out.println("Pizza");
            break;
            default: System.out.println("nothing to want");

        }
    }
    
}
